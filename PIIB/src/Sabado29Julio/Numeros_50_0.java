package Sabado29Julio;

/*
    * Realiza un programa que muestre los numeros del 50 - 0
 */
public class Numeros_50_0 {
    public static void main(String[] args) {
        System.out.println("Numeros del 50 - 0");
        for(int i = 50; i >= 0; i--){
            System.out.println("Numero: "+ i);
        }
    }
}
