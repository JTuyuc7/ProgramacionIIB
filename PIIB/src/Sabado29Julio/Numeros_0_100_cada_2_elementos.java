package Sabado29Julio;

/*
    * Realizar un programa que muestre los numeros del 0 - 100 cada 2 elementos
 */
public class Numeros_0_100_cada_2_elementos {
    public static void main(String[] args) {

        for(int i = 0; i <= 100; i+=2){
            System.out.println("Numero: "+ i);
        }
    }
}
