package Sabado29Julio;
/*
    * Realizar un programa que muestre los numeros del 1 - 50
 */

public class Numeros_1_50 {
    public static void main(String[] args) {
        System.out.println("Mostrar los numeros del 1 - 50");
        for(int i = 1; i <= 50; i++){
            System.out.println("Numero: " + i);
        }
    }
}
