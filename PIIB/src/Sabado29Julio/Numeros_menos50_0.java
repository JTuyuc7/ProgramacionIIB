package Sabado29Julio;
/*
    * Realizar un programa que mustre los numeros del -50 al 0
 */
public class Numeros_menos50_0 {
    public static void main(String[] args) {
        System.out.println("Mostrar los numeros del -50 - 0");
        for( int i = -50; i <= 0; i++){
            System.out.println("Numero: " +i);
        }
    }
}
